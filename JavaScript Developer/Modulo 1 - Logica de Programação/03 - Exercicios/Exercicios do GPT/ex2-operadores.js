let maiorQue = numero1 > numero2;

console.log(`Soma: ${soma}`);
console.log(`Subtração: ${subtracao}`);
console.log(`Multiplicacao: ${multiplicacao}`);
console.log(`Divisao: ${divisao}`);
console.log(`O primeiro é maior que o segundo? ${maiorQue}`);
